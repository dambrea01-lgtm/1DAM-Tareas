    private void lblAreaDibujoMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAreaDibujoMouseDragged
        
        // Obtenemos el pincel seleccionado
        Pincel pincel = (Pincel)cmbPinceles.getSelectedItem();
        if(pincel == null) 
            return;

        // Obtenemos el Graphics del lienzo
        Graphics g = lienzo.getGraphics();

        // Color actual del botón cmdColor
        Color color = cmdColor.getBackground();
        g.setColor(color);

        // Obtenemos la posición del ratón
        Point punto = evt.getPoint();

        // Dibujamos usando el pincel
        pincel.dibujar(g, punto);

        g.dispose();
        actualizarPantalla();
        
    }//GEN-LAST:event_lblAreaDibujoMouseDragged

    private void cmbPincelesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbPincelesItemStateChanged
        Pincel pincel = (Pincel) cmbPinceles.getSelectedItem();
        if(pincel != null) 
            pincel.resetear();
    }//GEN-LAST:event_cmbPincelesItemStateChanged

