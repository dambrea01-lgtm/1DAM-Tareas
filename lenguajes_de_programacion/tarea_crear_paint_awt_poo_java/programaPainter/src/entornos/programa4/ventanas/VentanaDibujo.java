    /**
    * Crea un nuevo lienzo del tamaño del área de dibujo.
    * Opcionalmente puede rellenarse con un color elegido por el usuario.
    */
   private void nuevaImagen(){
       // Inicializamos el lienzo del mismo tamaño que el área de dibujo
       lienzo = new BufferedImage(lblAreaDibujo.getWidth(),
                                  lblAreaDibujo.getHeight(),
                                  BufferedImage.TYPE_INT_RGB);


       // Rellenamos el fondo de blanco
        Graphics g = lienzo.getGraphics();
        g.setColor(Color.WHITE);
        g.fillRect(0, 0, lienzo.getWidth(), lienzo.getHeight());
        g.dispose();

        // Mostramos el lienzo en pantalla
        actualizarPantalla();
   }

    /**
     * Actualiza el contenido del JLabel de dibujo con la imagen actual.
     */
    private void actualizarPantalla() {
        Graphics g = lblAreaDibujo.getGraphics();
        g.drawImage(lienzo, 0, 0, null);
        g.dispose();
    }
    
    /**
    * Rellena el combo con los diferentes tipos de pinceles.
    */
   private void rellenarPinceles() {
       cmbPinceles.removeAllItems(); // limpiamos por si acaso

       cmbPinceles.addItem(new PincelBasico());
       cmbPinceles.addItem(new PincelContinuo());
       cmbPinceles.addItem(new PincelGordo());
       cmbPinceles.addItem(new PincelRectangulo());
       cmbPinceles.addItem(new PincelEstrella());
   }

   
    
